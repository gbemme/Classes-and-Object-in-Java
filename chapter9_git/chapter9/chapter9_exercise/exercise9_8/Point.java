package exercise9_8;

public class Point {
private double x;
private double y;

public Point(double x,double y) {
	this.x=x;
	this.y=y;
}


public double getX() {
	return x;
}
public void setX(double x1 ) {
	this.x = x1;

}
public double getY() {
	return y;
}
public void setY(double y2) {
	this.y = y2;
	
}
	
}
